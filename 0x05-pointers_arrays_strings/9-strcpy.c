#include "main.h"

/**
 * _strcpy - function that copies the string pointed to by src,
 * including the terminating null byte  ('\0'), to the buffer
 * pointed  to  by  dest.
 * @src: character pointer
 * @dest: character pointer
 *
 * Return: Return dest
 */
char *_strcpy(char *dest, char *src)
{
	_strcpy(dest, src);
	return (dest);
}
